CREATE TABLE ITEM(code INT(8) NOT NULL, price INT(8) NOT NULL, PRIMARY KEY(code));
CREATE TABLE BOOK(ISBN INT NOT NULL, title VARCHAR(50) NOT NULL, publisher VARCHAR(50), PRIMARY KEY(ISBN));
CREATE TABLE DVD(dvd_number INT NOT NULL, producer VARCHAR(50), PRIMARY KEY(dvd_number));
CREATE TABLE Ship_date(date INT NOT NULL);
CREATE TABLE SHIPMENT(quantity INT NOT NULL);
CREATE TABLE ORDERS(onumber INT(10) NOT NULL, order_date INT NOT NULL, discount INT, PRIMARY KEY(onumber));
CREATE TABLE CUSTOMER(phone INT NOT NULL, first_name VARCHAR(50), last_name VARCHAR(50), email VARCHAR(50), hobby VARCHAR(50), PRIMARY KEY(phone));
CREATE TABLE COUNTRY(cname VARCHAR(50), PRIMARY KEY(cname));
